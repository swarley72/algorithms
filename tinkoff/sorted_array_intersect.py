# Даны 2 отсортированных массива вывести все элементы первого массива
# которые не встречаются во втором


def get_diff(arr1: list[int], arr2: list[int]):
    l, r = 0, 0
    res = []
    while l < len(arr1) and r < len(arr2):
        if arr1[l] == arr2[r]:
            l += 1
            r += 1
        elif arr1[l] < arr2[r]:
            res.append(arr1[l])
            l += 1
        else:
            r += 1

    while l < len(arr1):
        res.append(arr1[l])
    return res


assert get_diff([1, 2, 3, 5], [1, 3, 4]) == [2, 5]
assert get_diff([1, 2, 3, 5], [1, 3, 4]) == [2, 5]
assert get_diff([1, 2, 3], [4, 5, 6]) == [1, 2, 3]  # нет пересечений
assert get_diff([1, 2, 3], [1, 2, 3]) == []  # полное совпадение

# Пустые массивы
assert get_diff([], [1, 2, 3]) == []  # первый пустой
assert get_diff([1, 2, 3], []) == [1, 2, 3]  # второй пустой
assert get_diff([], []) == []  # оба пустые

# Один элемент
assert get_diff([5], [5]) == []  # совпадает
assert get_diff([5], [3]) == [5]  # не совпадает
assert get_diff([5], [7]) == [5]  # второй больше
assert get_diff([5], [1]) == [5]  # второй меньше

assert get_diff([1, 1, 2, 2, 3], [1, 2]) == [1, 2, 3]  # дубликаты в первом
assert get_diff([1, 2, 3], [1, 1, 2, 2, 3]) == []  # дубликаты во втором
assert get_diff([1, 1, 1], [1]) == [1, 1]  # много дубликатов в первом
assert get_diff([1, 2, 2, 3, 3, 3], [2, 3]) == [1, 2, 3, 3]  # смешанные дубликаты

# Разные размеры
assert get_diff([1], [1, 2, 3, 4, 5]) == []  # первый намного меньше
assert get_diff([1, 2, 3, 4, 5], [3]) == [1, 2, 4, 5]  # второй намного меньше
assert get_diff([1, 2, 3, 4, 5, 6, 7], [2, 4, 6]) == [1, 3, 5, 7]  # через один

# Крайние значения
assert get_diff([1, 2, 3], [0]) == [1, 2, 3]  # второй меньше всех
assert get_diff([1, 2, 3], [10]) == [1, 2, 3]  # второй больше всех
assert get_diff([5, 10, 15], [1, 2, 3, 4]) == [5, 10, 15]  # второй весь меньше
